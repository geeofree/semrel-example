/**
 * Adds two numbers
 * @param {number} a
 * @param {number} b
 * @returns {number} a + b
 **/
const add = (a, b) => b + a;

/**
 * Subtracts two numbers
 * @param {number} a
 * @param {number} b
 * @returns {number} a - b
 **/
const sub = (a, b) => a - b;

/**
 * Multiples two numbers
 * @param {number} a
 * @param {number} b
 * @returns {number} a * b
 **/
const mul = (a, b) => a * b;

module.exports = {
  add,
  sub,
  mul,
};
